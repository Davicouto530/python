# lower = converte para caracteres minúsculos
sexo = input("Digite F se for mulher ou digite M se for masculino: ").lower()

# sexo = sexo.lower()
if sexo.lower() == "f":
    print("Feminino")
elif sexo.lower() == "m":
    ("Masculino")
else:
    print("Valor digitado inválido")
